/**
 * Enumeration of member roles in the rhythmic dance club.
 */
public enum MemberRole {
  /** Standard club member. */
  MEMBER,
  /** Secretary role for administrative actions. */
  SECRETARY,
  /** Teacher role to plan and manage classes. */
  TEACHER,
  /** President role to access platform statistics. */
  PRESIDENT
}
